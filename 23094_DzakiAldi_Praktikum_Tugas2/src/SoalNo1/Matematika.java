package SoalNo1;

public class Matematika {
    public void pertambahan(int a, int b){
        int jumlah = a + b;
        System.out.println(a + " + " + b + " = " + jumlah);
    }

    public void pengurangan(int a, int b){
        int jumlah = a - b;
        System.out.println(a + " - " + b + " = " + jumlah);
    }

    public void perkalian(int a, int b){
        int jumlah = a * b;
        System.out.println(a + " * " + b + " = " + jumlah);
    }

    public void pembagian(int a, int b){
        int jumlah = a / b;
        System.out.println(a + " / " + b + " = " + jumlah);
    }
}
